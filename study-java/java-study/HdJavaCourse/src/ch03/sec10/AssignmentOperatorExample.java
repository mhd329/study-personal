package ch03.sec10;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		int res = 0;
		res += 10;
		System.out.println(res);
		res -= 5;
		System.out.println(res);
		res *= 3;
		System.out.println(res);
		res /= 5;
		System.out.println(res);
		res %= 3;
		System.out.println(res);

	}

}
