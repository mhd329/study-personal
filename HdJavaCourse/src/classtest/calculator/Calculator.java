package classtest.calculator;

public class Calculator {

	public void plus(double val1, double val2) {
		double result = val1 + val2;
		System.out.println(result);
	}
	public void minus(double val1, double val2) {
		double result = val1 - val2;
		System.out.println(result);
	}
	public void multiply(double val1, double val2) {
		double result = val1 * val2;
		System.out.println(result);
	}
	public void divide(double val1, double val2) {
		double result = val1 / val2;
		System.out.println(result);
	}
	public void rest(double val1, double val2) {
		double result = val1 % val2;
		System.out.println(result);
	}
	
	public void plus(int val1, int val2) {
		int result = val1 + val2;
		System.out.println(result);
	}
	public void minus(int val1, int val2) {
		int result = val1 - val2;
		System.out.println(result);
	}
	public void multiply(int val1, int val2) {
		int result = val1 * val2;
		System.out.println(result);
	}
	public void divide(int val1, int val2) {
		int result = val1 / val2;
		System.out.println(result);
	}
	public void rest(int val1, int val2) {
		int result = val1 % val2;
		System.out.println(result);
	}
	
}
